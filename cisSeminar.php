                                                        <h4 class="elh-event-title"><a href="#">SCALING CRYPTOCURRENCIES</a></h4>
                                                        <p class="elh-event-excerpt">Derek will speak about minimizing storage and boostrapping costs in the Algorand cryptocurrency. <br><a href="javascript:void(0);"  onclick="test3()">Read abstract and more information</a><br/><br/>
                                                            <h5>Abstract</h5>
                                                            Decentralized cryptocurrencies rely on participants to keep track of the state of the system in order to verify new transactions. As the number of users and transactions grows, this requirement becomes a significant burden, requiring users to download, verify, and store a large amount of data to participate.
                                                            Vault is a new cryptocurrency design based on Algorand that minimizes these storage and bootstrapping costs for participants. Vault’s design is based on Algorand’s proof-of-stake consensus protocol and uses several techniques to achieve its goals. First, Vault decouples the storage of recent transactions from the storage of account balances, which enables Vault to delete old account state. Second, Vault allows sharding state across participants in a way that preserves strong security guarantees. Finally, Vault introduces the notion of stamping certificates, which allow a new client to catch up securely and efficiently in a proof- of-stake system without having to verify every single block.<br/>
                                                            Experiments with a prototype implementation of Vault’s data structures show that Vault’s design reduces the bandwidth cost of joining the network as a full client by 99.7% compared to Bitcoin and 90.5% compared to Ethereum when downloading a ledger containing 500 million transactions.
                                                            Paper URL: <a htref="https://people.csail.mit.edu/nickolai/papers/leung-vault.pdf">https://people.csail.mit.edu/nickolai/papers/leung-vault.pdf</a>.</p>
                                                        <h5>Bio</h5>
                                                        <p>
                                                            Derek Leung is currently a software engineer at Algorand, a cryptocurrency startup. He received a M.S. in EECS from MIT in 2018 and was advised by Professor Nickolai Zeldovich. There he worked on Stadium, a scalable messaging system with strong metadata privacy guarantees. Previously he earned a B.A. in Computer Science and Mathematics from UC Berkeley in 2016. At Berkeley he worked with Professor David Wagner on usable security.<br/><b><a hraf="javascript:void(0);" onclick="test3()">Minify</a></b> 
