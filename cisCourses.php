                                       <h3>CIS RELATED COURSES AT MIT</h3>
                                       <br>
                                        <h5>CURRENT COURSES</h5>

                                        <h5>6.876J <a href="https://www.eecs.mit.edu/academics-admissions/academic-information/subject-updates-ft-2015/6876j-0" target="_blanck">Advanced Topics in Cryptography:  Lattices and Applications</a></h5>
                                        <iframe width="600" height="350" allowfullscreen="allowfullscreen" src="https://www.youtube.com/embed/jDsfV2ohFPs">
                                        </iframe><br/>
                                        <b>Professor:</b> V. Vaikuntanathan

                                        <p>Integer lattices are powerful mathematical objects that have found applications in many diverse facets of computer science, most notably in the areas of cryptography and combinational optimization.  This course gives an introduction to the theory of integer lattices--algorithms and applications to combinatorial optimization, their recent use in cryptography culminating in the first construction of a fully homomorphic encryption scheme, and the fascinating complexity landscape associated with lattice problems.</p>

                                        <h5>PREVIOUS COURSES</h5>

                                        <h5>6.892 <a href="http://www.eecs.mit.edu/academics-admissions/academic-information/subject-updates-ft-2013/6892" target="_blanck">Computing on Encrypted Data</a></h5>
                                        <iframe width="600" height="350" allowfullscreen="allowfullscreen" src="https://www.youtube.com/embed/jDsfV2ohFPs">
                                        </iframe><br/>
                                        <b>Professor:</b> V. Vaikuntanathan

                                        <p>We will study both classical and modern technologies for computing on encrypted data including, secure two-party and multi-party computation protocols, fully homomorphic encryption, functional encryption and program obfuscation. We will discuss the recent exciting developments in these areas, and discuss several open problems.</p>

 

                                        <h5>6.857 <a href="http://courses.csail.mit.edu/6.857/2013/" target="_blanck">Network and Computer Security </a></h5>
                                        <iframe width="600" height="350" allowfullscreen="allowfullscreen" src="https://www.youtube.com/embed/jDsfV2ohFPs">
                                        </iframe><br/>
                                        <b>Professor:</b> R. L. Rivest

                                        <p>Techniques for achieving security in multi-user computer systems and distributed computer systems. Topics: physical security; discretionary and mandatory access control; biometrics; information-flow models of security; covert channels; elementary cryptography; public-key cryptography; logic of authentication; electronic cash; viruses; firewalls; electronic voting; risk assessment; secure web browsers. </p>

                                         

                                        <h5>6.875J <a href="http://stellar.mit.edu/S/course/6/sp13/6.875/" target="_blanck">Cryptography and Cryptanalysis</a>  (Same subject as 18.425J)</h5>
                                        <iframe width="600" height="350" allowfullscreen="allowfullscreen" src="https://www.youtube.com/embed/jDsfV2ohFPs">
                                        </iframe><br/>
                                        <b>Professors:</b> S. Goldwasser, S. Micali

                                        <p>A rigorous introduction to modern cryptography. Emphasis on the fundamental cryptographic primitives of public-key encryption, digital signatures, pseudo-random number generation, and basic protocols and their computational complexity requirements.</p>

                                         

                                        <h5>6.876J Advanced Topics in Cryptography</a> (Same subject as18.426J)</h5>

                                        <b>Professors:</b> S. Goldwasser, S. Micali

                                        <p>Recent results in cryptography, interactive proofs, and cryptographic game theory. Lectures by instructor, invited speakers, and students.</p>

                                        <h5>6.S898 <a href="http://www.eecs.mit.edu/academics-admissions/academic-information/subject-updates-ft2012/6s898" target="_blanck">Evolution of a Proof</a></h5>
                                        <iframe width="600" height="350" allowfullscreen="allowfullscreen" src="https://www.youtube.com/embed/jDsfV2ohFPs">
                                        </iframe><br/>
                                        <b>Professor:</b> S. Goldwasser

                                        <p>This subject has been approved as either an AUS or as a Theoretical Computer Science concentration subject. Efficient proof systems in (1) complexity theory including interactive proofs, efficient arguments, cs-proofs, probabilistically checkable proofs and delegation systems with applications to checking correctness of untrusted computation and memory storage in the cloud and approximation hardness; (2) cryptography: zero knowledge interactive and non-interactive proofs; and (3) quantum computation: interactive proofs and multi-proven interactive proofs with classical and quantum verifiers. The emphasis will be on current state of the art as well as research directions.</p>

                                        <h5>6.889 New Developments in Cryptography</a></h5>
                                        <iframe width="600" height="350" allowfullscreen="allowfullscreen" src="https://www.youtube.com/embed/jDsfV2ohFPs">
                                        </iframe><br/>
                                        <b>Professor:</b> S. Goldwasser


                                        <p>The last few years have witnessed dramatic developments in the field of theoretical cryptography. Novel solutions to long standing open problems such as the existence Homomorphic Encryption have been found, new security concerns such as Leakage Resilience have been addressed, and a surge of new tools have been brought into the cryptographic arena. This development is motivated in part by technological advancement such as cloud computing platform and the abundant use of hand held peripheral devices.</p>

