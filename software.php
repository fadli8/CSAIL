                                        <h5>DOMESTIC WEB SERVER</h5>
                                        <p>If you are a member of the CIS group, and have some software that falls under the US export laws that you would like to publish on the Web, read <a href="http://groups.csail.mit.edu/cis/cis-publishing-sw.html" target="_blanck">this page</a>. It describes the Domestic Web Server, which can limit the distribution of export-controlled software to those users who represent themselves as eligible to receive it. The <a href="fredette@theory.lcs.mit.edu" target="_blanck">author</a> of the dws believes this due diligence is sufficient to satisfy current US export laws.</p>

                                         
                                        <h5>ELECTRONIC VOTING---EVOX</h5>
                                        <p>See the <a href="http://groups.csail.mit.edu/cis/voting/voting.html" target="_blanck">project web site</a>.</p>

                                         
                                        <h5>CRYPTOGRAPHIC PROTOCOL LANGUAGE</h5>
                                        <p>CPL is a simple, near-English language for expressing cryptographic protocols. An accompanying compilier translates protocols expressed as CPL "code" into working Java clients. CPL is also packaged with a tool for converting protocol descriptions into LaTeX diagrams.CPL lowers the barrier between abstract mathematical descriptions and working code. Potential uses include protocol design, code prototyping and functioning as a teaching aid.</p>

                                        <p>See the <a href="http://groups.csail.mit.edu/cis/cpl/index.html" target="_blanck">project web site</a>. </p>

